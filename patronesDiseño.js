// Patrones de Diseño
// Son soluciones reutilizables a problemas comunes de diseño de software
// No son fragmentos concretos de código que puedas copiar y pegar literalmente,
// sino conceptos, esquemas o "plantillas mentales" que te indican cómo organizar tus clases,
// objetos y funciones para resolver una problemática arquitectónica específica.

// Los patrones clásicos se agrupan en tres grandes categorías según su propósito fundamental:

// Creacionales: Se centran en la creación de objetos, encargándose de cómo se instancian las clases y qué mecanismos se utilizan para generar nuevos elementos.
// Estructurales: Se ocupan de cómo se relacionan y combinan las clases y objetos para formar estructuras más grandes y complejas.
// Comportamentales: Se enfocan en la comunicación, interacción y responsabilidad entre los objetos, definiendo cómo se comportan en diferentes escenarios.

